/**
 * Resize mode for a TextArea
 * @public
 */
export enum TextAreaResize {
    /**
     * No resize.
     */
    none = "none",

    /**
     * Resize vertically and horizontally.
     */
    both = "both",

    /**
     * Resize horizontally.
     */
    horizontal = "horizontal",

    /**
     * Resize vertically.
     */
    vertical = "vertical",
}
